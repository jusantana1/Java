package exercicio1;

public class RedeHotelaria {

    private String hotel;
    private int qnt;
    
    RedeHotelaria() {}

   public void setHotel(String hotel){
    this.hotel = hotel;
   } 
   public void setQnt(int qnt){
    this.qnt = qnt;
   }
   public String getHotel(){
    return hotel;
   }
   public int getQnt(){
    return qnt; 
   }
    
}
